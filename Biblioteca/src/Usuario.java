public class Usuario {
    public String nombreusuario;
    public String apellidos;
    public String dni;
    public int year;

    public Usuario(String nombreusuario,String apellidos, String dni, int year){
        this.nombreusuario=nombreusuario;
        this.apellidos=apellidos;
        this.dni=dni;
        this.year=year;
    };

}
